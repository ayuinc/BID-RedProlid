												<h4 class="text-gray-dark bold text-left pt-21">Usted utiliza las redes sociales, tiene cuentas en Twitter y Facebook. ¿Cree que las redes sociales deben cumplir un rol en el trabajo cotidiano de la mujer del sector público (para acercarse a la ciudadanía  e intercambiar sobre asuntos de interés, o  para visibilizar su trabajo, entre otros)?</h4>
												<p class="rem-10 text-gray text-justify">
													Evidentemente hoy no se puede prescindir de las herramientas, los formatos y los instrumentos que representan estas nuevas tecnologías de la comunicación. Creo que es esencial que toda persona que actúa en el ámbito público (y no solamente las mujeres) use estas redes, tratando de cumplir un principio fundamental de la mediación política, cual es la información. En otras palabras, estas herramientas sirven para cumplir nuestra obligación de informar sobre nuestras actividades, pero también para reflexionar y difundir mensajes claves sobre temas estratégicos que están en la mesa o para posicionarlos en la agenda política. Las organizaciones políticas tienen que echar mano de estas herramientas porque la función de la política es agregar criterios diversos, tratar de cohesionar, integrar y generar una corriente de opinión en torno a grandes temas.”

													Las redes pueden servir también para revertir un problema conocido: muchas veces las mujeres no son “visibles” en los medios de comunicación tradicionales. Me refiero a esa especie de miopía, por la cual los reporteros y los medios de comunicación, tanto de televisión como radio, parecen incapaces de ver a las mujeres que están en la cancha. Entonces esta suerte de discriminación, de invisibilización de las mujeres puede ser revertida a través de estos medios alternativos, en los cuales uno tiene el control sobre el mensaje y la serenidad suficiente para saber exactamente qué palabras usar.

													Está además la cuestión del gobierno abierto y el gobierno electrónico, hoy en boga. Son modelos de gestión que los  gobiernos deberán asumir. Ello implica respetar genuinamente el derecho a la libre expresión y al libre acceso a la información pública. Las mujeres tenemos que montarnos en esta corriente, asumir un liderazgo, ser un referente que valga la pena escuchar. Ahí las redes nos sirven para diseminar nuestro mensaje y la información ayudando a  construir y fortalecer nuestra relación con la sociedad, clave para el desarrollo de nuestro liderazgo. A mí me parece sensacional, además de divertido, porque el twitter es divertido. El fácil acceso  a toda la información que está circulando es genial  pero implica un desafío saber discernir y priorizar aquella relevante y pertinente a nuestros intereses para incorporarla a nuestros archivos personales
