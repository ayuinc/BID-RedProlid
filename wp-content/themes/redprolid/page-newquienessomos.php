							 	 		<h4 class="text-gray-dark bold text-left pt-21">En su opinión, ¿se está cumpliendo la Ley de Paridad en Bolivia?</h4>
												<p class="rem-10 text-gray text-justify">En Bolivia sí se cumplió con la Ley de Paridad en las últimas elecciones del 12 de octubre de 2014, tal como quedó en evidencia en la conformación de la Asamblea Legislativa Plurinacional. Las cifras son elocuentes: en la Asamblea Legislativa las mujeres tienen una representación incluso mayor que los hombres: 63 diputadas frente a 60 diputados (51%-49%). En cuanto a la Cámara Alta, las mujeres ocupan 16 bancas (44%). El cumplimiento efectivo de la paridad orienta el nuevo concepto  e idea de la democracia paritaria que queremos construir en el ámbito de la representación política.  Por estos resultados, Bolivia  está entre los primeros países que materializó la paridad, y este hecho nos convierte en un referente regional reconocido por la Unión Interparlamentaria, junto a otros países de la región, como Ecuador y Costa Rica. Sin embargo, reitero que la política y la democracia nos obliga a plantear metas y objetivos de participación que superen la aritmética simple.</p>
							 	 		<h4 class="text-gray-dark bold text-left pt-21">¿Cuáles son las tres estrategias que deben implementarse para que una ley de paridad funcione? </h4>
							 	 		<p class="rem-10 text-gray text-justify">
														En Bolivia, la paridad efectiva y la igualdad sustantiva lograda en la representación política de mujeres  es el resultado de un proceso histórico de acumulación y de desarrollo estratégico del movimiento de las mujeres que nació en el año 1995. Fue entonces cuando comenzamos a diseñar  la estrategia en favor de la implantación de medidas de acción afirmativa a través de la ley de cuotas. Las lucha por las cuotas y el 30 por ciento de inclusión obligatoria de mujeres en las listas de candidatos  fue la primera fase de este proceso. Se avanzó gracias a un pacto plural y exitoso de mujeres, más allá de los partidos y más allá de las sensibilidades políticas entre facciones. Entonces las mujeres, organizadas en el Foro Político,  apostamos por un pacto unitario de mujeres políticas y de organizaciones no gubernamentales y sociales feministas y no feministas, en torno a una agenda concreta a fin de lograr la aceptación de las cuotas.  Conviene recordar que ya en ese momento  teníamos claro que la paridad era el horizonte estratégico al cual debíamos llegar. Fue en 2004 cuando se incorporó por vez primera la paridad en una norma legislativa y se la aplico al proceso de apertura del sistema político a agrupaciones ciudadanas y a pueblos indígenas. Tomó cinco años hasta que la paridad se incorporó a la reforma constitucional de 2009, pero detrás había todo un proceso de acumulación de mujeres que hacen una masa crítica exitosa, movilizada y que permiten una incidencia efectiva en los distintos órganos de poder y logran despertar la voluntad política de quienes son los decisores.

														El segundo elemento fundamental es el rol del sistema electoral y del órgano que lo rige: cuando en 2009 se aplicó legalmente la paridad, detectamos limitaciones muy serias en nuestro sistema electoral para producir la paridad bajo el sistema mayoritario de elección. Ante ello, el 2014 el órgano electoral generó un reglamento por el cual se introduce el nuevo concepto de paridad horizontal en el bloque de candidaturas que son unipersonales en distritos uninominales y que responden a un sistema electoral mayoritario (y que en el mundo han resultado ser los más complicados para introducir cuotas y paridad). Esa “paridad horizontal”  obligaba a que el 50% de candidaturas uninominales del 100% de distritos unipersonales a elegirse por sistema de mayoría simple, tenían que ser mujeres. Es decir, la voluntad política, acompañada de condiciones imperativas y obligatorias para los partidos por parte de los administradores de procesos electorales es clave. Un ejemplo es que se devolvían las listas presentadas en caso de que no se cumpliera la paridad, y esto ha sido determinante para acercarnos a nuestra meta.

														En tercer lugar, yo destacaría la necesidad de desarrollar una capacidad de análisis permanente del sistema político, del comportamiento del voto, de los partidos políticos y del sistema electoral. Una vez que las mujeres ya estamos en cancha y hemos logrado el 50%, los desafíos a futuro no dejan de ser menores; es más, nos plantea nuevos retos, que consisten en pasar de la igualdad aritmética que propicia la paridad  a una representación sustantiva, cualitativa y no meramente simbólica; esto es, la legitimidad no se consigue solo con la presencia de mujeres, sino que necesitamos generar modelos de liderazgo exitosos en el ámbito del desempeño político.

														Hay varios obstáculos que limitan la construcción de pactos exitosos entre mujeres: el primero es la polarización del clima político en una lógica amigo-enemigo que marca la relación entre fuerzas políticas. En esos contextos los acuerdos multipartidarios y plurales son imposibles.

      								El otro obstáculo es el caudillismo y la personalización del poder, tan propios de nuestra cultura política latinoamericana, que normalmente muestra al caudillo con rostro masculino,  y finalmente las prácticas políticas clientelares y prebendales muy masculinas y arraigadas en nuestras sociedades.							 	 			
